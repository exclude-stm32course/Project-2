/* TEMPERATURE */
#define ADC_BITS (1 << 12) /* 12 bit adc = 4096 */
#define VCC 3.3f
#define BITS_PER_VOLT (ADC_BITS/VCC)
#define V25 (1.43f*BITS_PER_VOLT)  /* 1.43 from datasheet */
